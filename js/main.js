
function initializePagination(tableId, paginationId, data, recordsPerPage = 3) {
    let currentPage = 1;
    const tableBody = document.getElementById(tableId);
    const pagination = document.getElementById(paginationId);

    function renderTable(page) {
        tableBody.innerHTML = "";
        let start = (page - 1) * recordsPerPage;
        let end = start + recordsPerPage;
        let paginatedItems = data.slice(start, end);

        paginatedItems.forEach(item => {
            let row = `<tr>
                <td>${item.name}</td>
                <td>${item.phone}</td>
                <td>${item.cnic}</td>
                <td>${item.address}</td>
                <td>${item.rate}</td>
                <td>${item.sku}</td>
                <td>${item.service_ids}</td>
                <td>${item.services}</td>
                <td>${item.api_tokens}</td>
                <td><button class="btn btn-sm btn-primary"><i class="bi bi-eye"></i></button></td>
            </tr>`;
            tableBody.innerHTML += row;
        });

        updatePagination();
    }

    function updatePagination() {
        let totalPages = Math.ceil(data.length / recordsPerPage);
        let paginationHTML = "";

        paginationHTML += `<li class="page-item ${currentPage === 1 ? "disabled" : ""}" data-page="prev">
            <a class="page-link" href="#">Previous</a></li>`;

        for (let i = 1; i <= totalPages; i++) {
            paginationHTML += `<li class="page-item ${currentPage === i ? "active" : ""}" data-page="${i}">
                <a class="page-link" href="#">${i}</a></li>`;
        }

        paginationHTML += `<li class="page-item ${currentPage === totalPages ? "disabled" : ""}" data-page="next">
            <a class="page-link" href="#">Next</a></li>`;

        pagination.innerHTML = paginationHTML;
        addPaginationEvents();
    }

    function addPaginationEvents() {
        document.querySelectorAll(`#${paginationId} .page-item`).forEach(item => {
            item.addEventListener("click", function (event) {
                event.preventDefault();
                let page = this.getAttribute("data-page");

                if (page === "prev" && currentPage > 1) {
                    currentPage--;
                } else if (page === "next" && currentPage < Math.ceil(data.length / recordsPerPage)) {
                    currentPage++;
                } else if (!isNaN(page)) {
                    currentPage = parseInt(page);
                }
                renderTable(currentPage);
            });
        });
    }

    renderTable(currentPage);
}


